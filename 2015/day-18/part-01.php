function updateLights($grid, $clone)
{
	foreach($grid as $coords => $status) {
		$grid[$coords] = checkNeighbours($clone, $coords);
	}
	return $grid;
}

function checkNeighbours($grid, $coords) {
	$base = $grid[$coords];
	$x = explode('/', $coords)[0];
	$y = explode('/', $coords)[1];
	$active = 0;
	if (array_key_exists(($x-1).'/'.$y, $grid)) {
		$active += $grid[($x-1).'/'.$y] === '#' ? 1 : 0;
	}
	if (array_key_exists(($x-1).'/'.($y+1), $grid)) {
		$active += $grid[($x-1).'/'.($y+1)] === '#' ? 1 : 0;
	}
	if (array_key_exists($x.'/'.($y+1), $grid)) {
		$active += $grid[$x.'/'.($y+1)] === '#' ? 1 : 0;
	}
	if (array_key_exists(($x+1).'/'.($y+1), $grid)) {
		$active += $grid[($x+1).'/'.($y+1)] === '#' ? 1 : 0;
	}
	if (array_key_exists(($x+1).'/'.$y, $grid)) {
		$active += $grid[($x+1).'/'.$y] === '#' ? 1 : 0;
	}
	if (array_key_exists(($x+1).'/'.($y-1), $grid)) {
		$active += $grid[($x+1).'/'.($y-1)] === '#' ? 1 : 0;
	}
	if (array_key_exists($x.'/'.($y-1), $grid)) {
		$active += $grid[$x.'/'.($y-1)] === '#' ? 1 : 0;
	}
	if (array_key_exists(($x-1).'/'.($y-1), $grid)) {
		$active += $grid[($x-1).'/'.($y-1)] === '#' ? 1 : 0;
	}

	if ($base === '#') {
		if ($active == 2 || $active == 3) {
			return '#';
		} else {
			return '.';
		}
	} elseif ($base === '.') {
		if ($active === 3) {
			return '#';
		} else {
			return '.';
		}
	}
}

