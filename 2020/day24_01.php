$lines = explode(PHP_EOL, $input);

$positions['0.0/0.0'] = 'w';

foreach ($lines as $line) {
  preg_match_all('/e|se|ne|w|sw|nw/', $line, $directions);
  $position = [
    'x' => 0.0,
    'y' => 0.0,
  ];
  foreach ($directions[0] as $direction) {
    switch ($direction) {
      case 'e':
        $position['x'] += 1.0;
        break;
      case 'se':
        $position['x'] += 0.5;
        $position['y'] -= 1.0;
        break;
      case 'ne':
        $position['x'] += 0.5;
        $position['y'] += 1.0;
        break;
      case 'w':
        $position['x'] -= 1.0;
        break;
      case 'sw':
        $position['x'] -= 0.5;
        $position['y'] -= 1.0;
        break;
      case 'nw':
        $position['x'] -= 0.5;
        $position['y'] += 1.0;
        break;
    }
  }

  $coords =
    number_format($position['x'], 1) . '/' . number_format($position['y'], 1);

  if (array_key_exists($coords, $positions)) {
    $positions[$coords] === 'b'
      ? ($positions[$coords] = 'w')
      : ($positions[$coords] = 'b');
  } else {
    $positions[$coords] = 'b';
  }
}

collect($positions)
  ->filter(function ($tile) {
    return $tile === 'b';
  })
  ->count();
